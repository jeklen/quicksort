# create 50 same numbers
for i in range(49):
    print(5, end='')
    print('H,', end = ' ')
    
print(5, end = '')
print('H', end = ' ')

print('')
# create 50 increasing numbers
for i in range(49):
    print(i, end='')
    print('H,', end = ' ')

print(49, end = '')
print('H', end = ' ')
print('')
# create 50 decreasing numbers
for i in range(49, 0, -1):
    print(i, end='')
    print('H,', end = ' ')

print(0, end = '')
print('H', end = ' ')
print('')
